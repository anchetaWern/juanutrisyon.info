      <h2>1. "Okay lang ang softdrinks basta maraming tubig kasunod."</h2>
      <p>
        We've all heard this one - or said it. The logic sounds reasonable: water flushes things
        out, so drinking enough of it should cancel out that extra can of Coke, right?
      </p>
      <p>
        Hindi ganon ka-simple ang katawan natin. A regular 330ml can of soda contains around 35
        grams of sugar - that's roughly 9 teaspoons. Once that sugar is in your bloodstream, your
        kidneys and liver have to process it regardless of how much water you drink afterward.
        Water helps with hydration, yes, but it does not "flush out" excess sugar or neutralize the
        metabolic load it creates.
      </p>
      <p>
        Over time, regular soda consumption is strongly associated with increased risk of type 2
        diabetes, fatty liver disease, and obesity - conditions that are already alarmingly common
        among Filipinos. The Department of Health has flagged non-communicable diseases like
        diabetes as a major public health concern in the country.
      </p>
      <p>
        What to do instead: Treat softdrinks as an occasional treat, hindi pang-araw-araw na
        inumin. Water, unsweetened juice, or even sago't gulaman (go light on the sugar) are better
        everyday options.
      </p>
    </section>

    <section class="post-section">
      <h2>2. "Itlog? Hindi ako kumakain niyan, may cholesterol kasi."</h2>
      <p>
        Eggs have had a rough reputation for decades. Because egg yolks contain dietary
        cholesterol, a lot of Filipinos - especially those with heart concerns - have been avoiding
        them entirely. Sayang naman, kasi itlog is one of the most affordable and nutritious foods
        available to us.
      </p>
      <p>
        Here's what research now tells us: for most healthy people, dietary cholesterol from whole
        foods like eggs has a relatively small effect on blood cholesterol levels. What raises your
        LDL ("bad") cholesterol more significantly is saturated fat and trans fat - the kind found
        in processed foods, fried snacks, and fatty cuts of meat.
      </p>
      <p>
        Eggs, on the other hand, are rich in protein, B vitamins, choline (important for brain
        health), and healthy fats. Multiple large studies have not found a significant link between
        moderate egg consumption (one egg a day) and increased cardiovascular disease risk in
        healthy individuals.
      </p>
      <p>
        What to do instead: Unless your doctor or dietitian has specifically told you to limit
        eggs due to a condition like familial hypercholesterolemia, one egg a day is generally
        fine. Worry more about what's cooked with the egg - layunin mong iwasan ang sinangag sa
        maraming mantika.
      </p>
    </section>

    <section class="post-section">
      <h2>3. "Nag-exercise naman ako, pwede na kumain ng matamis."</h2>
      <p>
        This is probably the most relatable one on this list. You walked 30 minutes on the
        treadmill, so a slice of ube cake is fair game, di ba?
      </p>
      <p>
        Unfortunately, exercise and diet don't quite work on a 1-to-1 barter system. A 30-minute
        moderate walk burns roughly 150 to 200 calories. A slice of ube cake with cream cheese
        frosting? Easily 400 to 500 calories. The math doesn't favor us.
      </p>
      <p>
        More importantly, the problem with excess sugar intake isn't just the calories - it's the
        insulin response, the potential for fat storage, and the effect on long-term metabolic
        health. Exercise is incredibly important for health, but it cannot fully compensate for a
        consistently poor diet.
      </p>
      <p>
        There's even a name for this thinking in behavioral science: the "licensing effect" -
        when doing something virtuous (like exercising) makes us feel entitled to an offsetting
        indulgence. It's a very human tendency, but it can quietly derail health goals.
      </p>
      <p>
        What to do instead: Think of exercise and nutrition as two separate pillars, hindi isang
        sistema ng palitan. Both matter. Reward yourself for exercising - just not always with
        food.
      </p>
    </section>

    <section class="post-section">
      <h2>4. "Ang kanin ang pumapayat. Kaya ko nang kumain ng mas marami nito."</h2>
      <p>
        Wait - rice makes you thin? This one actually exists, and it comes from a kernel of truth
        twisted into misinformation. Some people observe that countries with high rice consumption
        (like Japan or South Korea) have lower obesity rates, and they conclude that rice must be
        fine to eat freely.
      </p>
      <p>
        The error is in ignoring context. Japanese and Korean dietary patterns typically include
        much smaller portion sizes overall, abundant vegetables, fermented foods, and less
        processed food - rice is just one part of a much more balanced picture.
      </p>
      <p>
        In the Philippine context, kanin is often consumed in very large portions, alongside
        high-sodium, high-fat ulam, and sweetened drinks. That combination - not rice alone -
        contributes to metabolic issues. White rice also has a relatively high glycemic index,
        which can cause blood sugar spikes when eaten in large amounts, a concern especially
        relevant given how common prediabetes and diabetes are in the Philippines.
      </p>
      <p>
        What to do instead: You don't need to eliminate rice - it's a cultural staple and a
        legitimate carbohydrate source. The goal is right-sized portions paired with enough
        vegetables, protein, and healthy fats. A helpful visual: your plate should be roughly half
        vegetables, a quarter protein, and a quarter rice or other carbs.
      </p>
    </section>

    <section class="post-section">
      <h2>5. "Healthy naman ang fresh fruit juice, araw-araw okay."</h2>
      <p>
        Fresh fruit juice sounds like a health win - walang preservatives, galing sa prutas,
        masustansya. But here's the thing: when you juice fruit, you extract the sugar while
        leaving behind most of the fiber. That fiber is actually what slows sugar absorption and
        keeps you full.
      </p>
      <p>
        A glass of orange juice can contain the sugar equivalent of 3 to 4 oranges, but it won't
        fill you up the way eating those oranges would. Taken daily in large amounts, fruit juice
        can contribute significantly to sugar intake - and it's easy to underestimate because it
        feels healthy.
      </p>
      <p>
        This doesn't mean fruit juice is poison. But treating it as a free-flowing daily drink,
        especially for children, is something many Filipino families unknowingly do.
      </p>
      <p>
        What to do instead: Eat the whole fruit whenever possible. If you love juice, treat it as
        an occasional drink, keep portions small (half a glass), and pair it with a meal.
      </p>
    </section>

    <section class="post-section">
      <h2>6. "Pag nagdidiyeta, huwag kumain pagkagising - para mas malaki ang calorie deficit."</h2>
      <p>
        Skipping breakfast as a weight loss strategy is common, but the reasoning behind it is
        often flawed. Many people think: fewer meals = fewer calories = more weight loss. In
        practice, skipping breakfast frequently leads to stronger hunger cravings later in the
        day, lower energy for activity, and poorer food choices at lunch and meryenda.
      </p>
      <p>
        For Filipinos with physically active mornings - commuting, manual labor, school - going
        without breakfast can also impair concentration and physical performance.
      </p>
      <p>
        That said, context matters. Structured intermittent fasting under the guidance of a
        dietitian is a different thing from casually skipping breakfast while not adjusting
        anything else. The difference is intentionality and proper planning.
      </p>
      <p>
        What to do instead: If you're trying to lose weight, focus on the quality and total amount
        of what you eat across the day rather than eliminating meals. A light but nutritious
        breakfast - like eggs, a banana, or oats - can actually help you eat less overall.
      </p>
    </section>

    <section class="post-section">
      <h2>7. "Ang mahal ng healthy food. Hindi kaya ng budget namin."</h2>
      <p>
        This one isn't entirely a myth - processed and fast food can sometimes feel cheaper and
        more convenient than fresh produce. But the belief that eating healthily requires a large
        budget keeps a lot of Filipino families from even trying.
      </p>
      <p>
        The truth is that some of the most nutritious foods are also among the most affordable in
        the Philippines: kamote (sweet potato), monggo, malunggay, sardinas, itlog, and local
        vegetables like ampalaya, kangkong, and pechay. These are packed with vitamins, minerals,
        protein, and fiber - at a fraction of the cost of processed snacks or fast food meals.
      </p>
      <p>
        The real cost of an unhealthy diet tends to show up later - in doctor's fees, medications,
        and lost productivity from preventable illness. Eating well is an investment, hindi
        gastos.
      </p>
      <p>
        What to do instead: Plan meals around affordable local foods. You don't need imported
        superfoods. Lutong-bahay built around whole grains, vegetables, legumes, and lean protein
        is both budget-friendly and genuinely nutritious.
      </p>
