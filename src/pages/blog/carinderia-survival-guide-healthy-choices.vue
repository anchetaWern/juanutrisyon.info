      <h2>3. The Sabaw Secret</h2>
      <p>
        The broth (sabaw) is a double-edged sword. On one hand, a warm bowl of Sinigang or Tinola
        broth can help you feel full faster, preventing overeating. On the other hand, many
        carinderia broths are heavily salted or use flavor cubes high in sodium.
      </p>
      <p>
        <strong>The Strategy:</strong> Ask for extra sabaw but try not to drown your rice in it.
        This gives you the volume and warmth of a heavy meal without the extra calories of a
        second cup of rice.
      </p>
