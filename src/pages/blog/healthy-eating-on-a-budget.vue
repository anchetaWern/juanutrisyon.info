      <div class="recipe-block">
        <h3>Recipe 5: Kamote and Itlog Hash (Budget Breakfast / Merienda)</h3>
        <p>
          Why it works nutritionally: Kamote provides complex carbohydrates and fiber that digest
          slowly, giving sustained energy. Eggs add protein and healthy fat. This keeps you full
          for hours - perfect bilang almusal or meryenda.
        </p>
        <p>Estimated cost for 2 servings: ₱50-65</p>
        <p>
          (Eggs at ₱8.19/pc x 2 = ~₱16.40; kamote not listed in the DA index but commonly
          ₱40-60/kg at wet market; garlic imported at ₱149.24/kg - use sparingly)
        </p>
        <h4>Ingredients</h4>
        <ul class="list">
          <li>2 medium kamote (sweet potato), peeled and diced small</li>
          <li>2 eggs</li>
          <li>3 cloves garlic, minced</li>
          <li>1/2 onion, sliced</li>
          <li>1 tbsp cooking oil</li>
          <li>Salt, pepper, and a pinch of paprika (optional)</li>
          <li>Green onions for topping</li>
        </ul>
        <h4>Instructions</h4>
        <ol class="steps">
          <li>Parboil or microwave diced kamote for 3-4 minutes until just tender. Drain.</li>
          <li>Heat oil in a pan. Saute garlic and onion until fragrant.</li>
          <li>
            Add kamote and spread into a single layer. Let it sit undisturbed for 2-3 minutes to
            get a little golden crust.
          </li>
          <li>Stir, season with salt, pepper, and paprika. Push to one side of the pan.</li>
          <li>Crack eggs into the empty side, scramble lightly, then mix everything together.</li>
          <li>Top with green onions and serve.</li>
        </ol>
        <p>
          Tip: This works as a whole meal on its own, or as a side. Leftovers reheat well the next
          morning.
        </p>
