      <div class="recipe-block">
        <h3>Recipe 3: Arroz Caldo (One-Pot Chicken and Rice Porridge)</h3>
        <p>
          Why it works nutritionally: Easy to digest, warming, and filling. Using bone-in chicken
          means you get natural collagen and minerals from the bones. A great option pag may
          nagkakasakit sa bahay - or for meryenda on a cold day.
        </p>
        <p>Estimated cost for 4-6 servings: ₱200-250</p>
        <p>
          (Chicken leg quarter unbranded at ₱202.80/kg; local regular milled rice at ₱43.27/kg;
          ginger at ₱90.00/kg; eggs at ₱8.19/pc)
        </p>
        <h4>Ingredients</h4>
        <ul class="list">
          <li>500g chicken, bone-in, cut into pieces (use cheaper cuts - neck, wings, or thighs)</li>
          <li>1 cup glutinous rice (malagkit) or regular rice</li>
          <li>6 cups water or broth</li>
          <li>1 thumb-sized piece of ginger, sliced</li>
          <li>5 cloves garlic, minced</li>
          <li>1 medium onion, sliced</li>
          <li>1 tbsp cooking oil</li>
          <li>Patis and pepper to taste</li>
          <li>Toppings: fried garlic, sliced green onions, hard-boiled egg, calamansi</li>
        </ul>
        <h4>Instructions</h4>
        <ol class="steps">
          <li>Saute garlic (reserve some for topping), onion, and ginger in oil until fragrant.</li>
          <li>Add chicken pieces and cook until lightly browned on all sides.</li>
          <li>Add rice and stir to coat in the oil and aromatics.</li>
          <li>
            Pour in water or broth. Bring to a boil, then lower heat and simmer, stirring
            occasionally, for 25-30 minutes until rice breaks down into a thick porridge
            consistency.
          </li>
          <li>Season with patis and pepper.</li>
          <li>Serve with fried garlic, green onions, a half boiled egg, and a squeeze of calamansi.</li>
        </ol>
        <p>
          Tip: This stretches beautifully - a big pot feeds the whole family and reheats well the
          next day with a little added water.
        </p>
      </div>

