        <h3>Recipe 4: Ginisang Kangkong with Tokwa and Bagoong</h3>
        <p>
          Why it works nutritionally: Kangkong (water spinach) is rich in iron, calcium, and
          vitamins A and C. Tokwa (tofu) is a complete plant protein. This dish is nutritionally
          dense and costs almost nothing.
        </p>
        <p>Estimated cost for 4 servings: ₱80-100</p>
        <p>
          (Kangkong at ₱72.14/bundle; firm tokwa typically ₱20-30/block at wet market; bagoong
          alamang ~₱20 for small portion)
        </p>
        <h4>Ingredients</h4>
        <ul class="list">
          <li>1 big bundle kangkong, washed, tough stems removed</li>
          <li>1 block firm tokwa (tofu), cubed</li>
          <li>3 cloves garlic, minced</li>
          <li>1 medium onion, sliced</li>
          <li>1 tbsp cooking oil</li>
          <li>1-2 tsp bagoong alamang (shrimp paste) - or patis if avoiding bagoong</li>
          <li>A little water if needed</li>
        </ul>
        <h4>Instructions</h4>
        <ol class="steps">
          <li>Fry tokwa cubes in oil until golden on the outside. Set aside.</li>
          <li>In the same pan, saute garlic and onion. Add bagoong and cook for 1 minute.</li>
          <li>Add kangkong stems first and stir-fry for 1-2 minutes. Then add the leaves.</li>
          <li>
            Return the tokwa to the pan, toss everything together, and cook until kangkong is just
            wilted - hindi sobrang luto, para hindi mawala ang nutrients.
          </li>
          <li>Serve immediately with rice.</li>
        </ol>
        <p>
          Tip: Tokwa absorbs flavor well - marinate it in a little soy sauce and calamansi before
          frying for more depth.
        </p>
