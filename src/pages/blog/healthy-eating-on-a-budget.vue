      <h2>5 Practical Tips to Stretch Your Food Budget Further</h2>
      <ol class="steps">
        <li>
          Palengke over supermarket for produce. Local wet markets almost always have fresher
          vegetables at lower prices. Go early for the best picks, or go late for bargains.
        </li>
        <li>
          Cook once, eat twice. Dishes like arroz caldo, monggo, and nilaga taste even better the
          next day. Plan for leftovers intentionally - it saves time and money.
        </li>
        <li>
          Dried and canned are not lesser options. Dried monggo, dilis, and canned sardines are
          shelf-stable, affordable, and nutritionally excellent. Stock these as pantry staples so
          you always have a nutritious fallback.
        </li>
        <li>
          Buy whole, not pre-cut. Pre-cut vegetables and boneless, skinless chicken cost more.
          Buying whole and prepping yourself takes a little more time but saves meaningfully over a
          week.
        </li>
        <li>
          Reduce, don't eliminate, rice. Rather than buying expensive rice substitutes, simply
          right-size your kanin portion and fill the rest of your plate with more vegetables and
          protein. Same staple, better balance.
        </li>
      </ol>
