      <div class="recipe-block">
        <h3>Recipe 1: Ginisang Monggo with Malunggay and Tinapa</h3>
        <p>
          Why it works nutritionally: Plant protein from monggo, omega-3s and flavor from tinapa,
          iron and vitamins from malunggay. A complete, satisfying meal.
        </p>
        <p>Estimated cost for 4 servings: ₱120-150</p>
        <p>
          (Monggo at ₱138.87/kg; sardinas Tamban at ₱141.25/kg; malunggay often free from backyard
          or ₱10-15/bundle at palengke; tomato at ₱61.81/kg)
        </p>
        <h4>Ingredients</h4>
        <ul class="list">
          <li>1 cup monggo (mung beans), soaked for 2 hours or overnight</li>
          <li>1 piece tinapa (smoked fish), flaked, bones removed</li>
          <li>1 cup malunggay leaves</li>
          <li>3 cloves garlic, minced</li>
          <li>1 medium onion, sliced</li>
          <li>2 medium tomatoes, chopped</li>
          <li>3 cups water</li>
          <li>1 tbsp cooking oil</li>
          <li>Fish sauce (patis) to taste</li>
        </ul>
        <h4>Instructions</h4>
        <ol class="steps">
          <li>Boil the soaked monggo in 3 cups of water until soft, about 20-25 minutes. Set aside with the liquid.</li>
          <li>In a pan, saute garlic and onion in oil until fragrant. Add tomatoes and cook until they soften.</li>
          <li>Add the flaked tinapa and stir for 1-2 minutes.</li>
          <li>Pour in the cooked monggo along with its liquid. Mix well and simmer for 5 minutes.</li>
          <li>Season with patis. Add malunggay leaves last, stir, and remove from heat.</li>
          <li>Serve with rice.</li>
        </ol>
        <p>
          Tip: Tinapa can be swapped for dilis (dried anchovies) or even a small can of sardinas -
          same umami punch, different budget options.
        </p>
      </div>

      <div class="recipe-block">
        <h3>Recipe 2: Sardinas Guisado with Ampalaya</h3>
        <p>
          Why it works nutritionally: Omega-3s from sardines, blood-sugar-regulating compounds from
          ampalaya (bitter gourd), plus the lycopene from tomatoes. Oo, may benefits talaga ang
          ampalaya kahit mapait.
        </p>
        <p>Estimated cost for 4 servings: ₱100-130</p>
        <p>
          (Sardinas Tamban at ₱141.25/kg; ampalaya at ₱123.03/kg; tomato at ₱61.81/kg; imported
          onion at ₱85.00/kg)
        </p>
        <h4>Ingredients</h4>
        <ul class="list">
          <li>1 can sardines in tomato sauce (255g)</li>
          <li>1 medium ampalaya, sliced thin, seeds removed</li>
          <li>3 cloves garlic, minced</li>
          <li>1 medium onion, sliced</li>
          <li>2 medium tomatoes, chopped</li>
          <li>1 tbsp cooking oil</li>
          <li>Salt and pepper to taste</li>
        </ul>
        <h4>Instructions</h4>
        <ol class="steps">
          <li>
            Optional: To reduce bitterness, rub ampalaya slices with salt and let sit for 10
            minutes, then rinse and squeeze out the liquid.
          </li>
          <li>Saute garlic and onion in oil. Add tomatoes and cook until soft.</li>
          <li>Add the ampalaya and stir-fry for 3-4 minutes until slightly tender but not mushy.</li>
          <li>Pour in the sardines (sauce and all). Mix gently so the fish doesn't break apart completely.</li>
          <li>Season to taste, simmer for 2-3 minutes, and serve.</li>
        </ol>
        <p>
          Tip: If ampalaya is not a family favorite, swap with sitaw (string beans) or kangkong.
          Same base, different vegetable - still nutritious.
        </p>
